function superar_nivel () {
    info.stopCountdown()
    info.changeScoreBy(1)
    info.changeLifeBy(1)
    game.showLongText("Nivel TUTORIAL superado!!", DialogLayout.Bottom)
    tiles.setCurrentTilemap(tilemap`nivel10`)
    tiles.placeOnTile(jugador, tiles.getTileLocation(5, 26))
    restablecer_gravedad()
}
function restablecer_gravedad () {
    controller.moveSprite(jugador, 75, 75)
    jugador.ay = 0
}
